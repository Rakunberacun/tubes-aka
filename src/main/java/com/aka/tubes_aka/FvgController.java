    public int findMaxFVG(ArrayList<Candle> candles){
        double maxGap = 0;
        int maxGapIndex = -1;

        for (int i = 2; i < candles.size(); i++) {
            double high1 = candles.get(i - 2).high;
            double low1 = candles.get(i - 2).low;
            double high3 = candles.get(i).high;
            double low3 = candles.get(i).low;

            // Check for bullish FVG
            if (low3 > high1) {
                double gap = low3 - high1;
                if (gap > maxGap) {
                    maxGap = gap;
                    maxGapIndex = i;
                }
            }

            if (high3 < low1) {
                double gap = low1 - high3;
                if (gap > maxGap) {
                    maxGap = gap;
                    maxGapIndex = i;
                }
            }
        }
        return maxGapIndex;
    }

    public int findMaxFVGRecursive(ArrayList<Candle> candles, int index, double maxGap, int maxGapIndex) {
        if (index >= candles.size()) {
            return maxGapIndex;
        }

        double high1 = candles.get(index - 2).high;
        double low1 = candles.get(index - 2).low;
        double high3 = candles.get(index).high;
        double low3 = candles.get(index).low;

        // Check for bullish FVG
        if (low3 > high1) {
            double gap = low3 - high1;
            if (gap > maxGap) {
                maxGap = gap;
                maxGapIndex = index;
            }
        }

        // Check for bearish FVG
        if (high3 < low1) {
            double gap = low1 - high3;
            if (gap > maxGap) {
                maxGap = gap;
                maxGapIndex = index;
            }
        }

        return findMaxFVGRecursive(candles, index + 1, maxGap, maxGapIndex);
    }    

